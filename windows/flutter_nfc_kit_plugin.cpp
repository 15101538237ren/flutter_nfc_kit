namespace flutter_nfc_kit {

// static
void FlutterNfcKitPlugin::RegisterWithRegistrar(
    flutter::PluginRegistrarWindows *registrar) {
  auto channel =
      std::make_unique<flutter::MethodChannel<flutter::EncodableValue>>(
          registrar->messenger(), "flutter_nfc_kit",
          &flutter::StandardMethodCodec::GetInstance());

  auto plugin = std::make_unique<FlutterNfcKitPlugin>();

  channel->SetMethodCallHandler(
      [plugin_pointer = plugin.get()](const auto &call, auto result) {
        plugin_pointer->HandleMethodCall(call, std::move(result));
      });

  registrar->AddPlugin(std::move(plugin));
}

FlutterNfcKitPlugin::FlutterNfcKitPlugin() {}

FlutterNfcKitPlugin::~FlutterNfcKitPlugin() {}

void FlutterNfcKitPlugin::HandleMethodCall(
    const flutter::MethodCall<flutter::EncodableValue> &method_call,
    std::unique_ptr<flutter::MethodResult<flutter::EncodableValue>> result) {
  if (method_call.method_name().compare("getPlatformVersion") == 0) {
    std::ostringstream version_stream;
    version_stream << "Windows ";
    if (IsWindows10OrGreater()) {
      version_stream << "10+";
    } else if (IsWindows8OrGreater()) {
      version_stream << "8";
    } else if (IsWindows7OrGreater()) {
      version_stream << "7";
    }
    result->Success(flutter::EncodableValue(version_stream.str()));
  } else {
    result->NotImplemented();
  }

  // TODO: implement method channel handlers for flutter_nfc_kit using pcsc-interface
  pcsc_interface::available();
}

}  // namespace flutter_nfc_kit
